class AreaTriangle{
   public static void main(String args[]) {   
	
	double base = 21.5; // this sets up the variable base with decimals
	double height = 64.87; // this sets up the variable height with decimals

    double area = (base * height)/2; // creates the area variable, setting it to the formula of area of a triangle using variables we created earlier
	
    System.out.println("Area of Triangle is: " + area);  // prints out the statement of the area of the triangle
    
    
   }
}